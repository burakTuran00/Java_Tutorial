public class VariablesOfString {
    public static void main(String[] args) {
        String name = "Rick";
        System.out.println("Hello " + name + "!");

        String a = "Java";
        String b = "Programming";
        String c = "Language";

        String sum = a + b + c;

        System.out.println(sum);

        String x = "Examine";
        int y = 45;

        String z = x + y;
        System.out.println(z);
    }
}
