public class ComparisonOperators {
    public static void main(String[] args) {
        /*
        == --> equal
        != --> not equal
        >  --> greater
        > --> less
        >= --> greater or equal
        <= --> less or equal
         */

        System.out.println(5 != 2); // ture
        System.out.println(4 > 5);  // false
        System.out.println(3 <= 3); // ture
    }
}
